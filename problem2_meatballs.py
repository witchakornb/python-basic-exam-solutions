# Problem 2: ลูกชิ้นเนื้อล้วนๆ
# โจทย์: ต้องการรู้ว่าจะต้องใช้ถุงลูกชิ้นกี่ถุงเพื่อปิ้งตามจำนวนไม้ที่ลูกค้าสั่ง
# แนวคิด:
# 1. รับจำนวนไม้ที่ลูกค้าสั่ง (m)
# 2. รับจำนวนลูกชิ้นต่อ 1 ไม้ (k)
# 3. รับจำนวนลูกชิ้นใน 1 ถุง (n)
# 4. ต้องการลูกชิ้นทั้งหมด = m * k
# 5. จำนวนถุงที่ต้องใช้ = ปัดขึ้น (ceil) ของ (ต้องการลูกชิ้นทั้งหมด / ลูกชิ้นต่อถุง)
#    เพื่อให้ทำได้แบบพื้นฐาน: ใช้วิธีตรวจว่าหารลงตัวไหม
#    - ถ้าหารลงตัว: เอาผลหารเลย
#    - ถ้าไม่ลงตัว: เพิ่มอีก 1 ถุง
# 6. แสดงผลเป็นจำนวนเต็ม

m = int(input())  # จำนวนไม้
k = int(input())  # ลูกชิ้นต่อไม้
n = int(input())  # ลูกชิ้นต่อถุง

needed = m * k  # ลูกชิ้นที่ต้องใช้

# คำนวณจำนวนถุงแบบไม่ใช้ math.ceil เพื่อให้ผู้เริ่มต้นเข้าใจง่าย
if needed % n == 0:
    bags = needed // n
else:
    bags = (needed // n) + 1

print(bags)
